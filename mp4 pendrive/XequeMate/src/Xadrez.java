    public void metodoEntrarLabel(char qualLetra, JLabel qualLabel) //recebe char e label
    {
        qualLabel.setBackground(Color.red); // coloca label vermelha
        qualLabel.setText(String.valueOf(qualLetra)); // coloca letra
        
        for (int contador = 0; contador < nome.length(); contador++)
            {
                if(qualLetra == nome.charAt(contador))
                {
                    qualLabel.setBackground(Color.red);
                    qualLabel.setText(String.valueOf(qualLetra));
                    break;
                }
                else
                {
                    continue;
                }
            }
    }
    
    public void metodoSairLabel(char qLetra, JLabel qLabel)
    {
        for (int contador = 0; contador < nome.length(); contador++)
        {
            if(qLetra == nome.charAt(contador))
            {
                qLabel.setBackground(Color.red);
                qLabel.setText(String.valueOf(qLetra));
                break;
            }
            else
            {                
                qLabel.setBackground(Color.blue);
                qLabel.setText("");
                continue;
            }
        }
    }
