class OperadoresAritimeticos{
   public static void main(String args[])
   {
     //Operadores Aritimédicos
     byte b1,b2, b3,b4;
          b1=1;b2=2;b3=3;b4=4;
      System.out.println(b1+b2);//operadores de adção
      System.out.println(b3-b2);//operadores de subtração
      System.out.println(b4*b3);//operadores de Multiplicação
      System.out.println(b2/b1);//opreradores de Divisão
      System.out.println(b4%b2);//operadores de Mod ou Resto
      System.out.println(-b1);//operadores de Sinal Negativo
      System.out.println("/n");
      System.out.println(+b2);
      }}